package bbs;

public class Bbs {

	private int bbsID;
	private String bbsTitle;
	private String bbsContents;
	private String bbsuserID;
	private String bbsData;
	private int bbsAvailable;
	
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getBbsContents() {
		return bbsContents;
	}
	public void setBbsContents(String bbsContents) {
		this.bbsContents = bbsContents;
	}
	public String getBbsuserID() {
		return bbsuserID;
	}
	public void setBbsuserID(String bbsuserID) {
		this.bbsuserID = bbsuserID;
	}
	public String getBbsData() {
		return bbsData;
	}
	public void setBbsData(String bbsData) {
		this.bbsData = bbsData;
	}
	public int getBbsAvailable() {
		return bbsAvailable;
	}
	public void setBbsAvailable(int bbsAvailable) {
		this.bbsAvailable = bbsAvailable;
	}
	
}
	